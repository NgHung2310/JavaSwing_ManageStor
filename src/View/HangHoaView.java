    public String getValueCbbLoaiHang() {
        for (Danhmuchang item : model.danhmuchangs) {
            if (item.getTenLoaiHang().equals(cbbLoaiHang.getSelectedItem().toString())) {
                return item.getMaLoaiHang().toString();
            }
        }
        return "";
    }
    
