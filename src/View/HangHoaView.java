    public void loadJTable() throws ClassNotFoundException, SQLException {
        int i = 0;
        model = new QuanLyKhoModel();
        for (Hanghoa item : model.hanghoas) {
            DefaultTableModel model = (DefaultTableModel) tbl.getModel();
            model.setRowCount(i + 1);
            tbl.setValueAt(item.getMaHang(), i, 0);
            tbl.setValueAt(item.getTenHang(), i, 1);
            tbl.setValueAt(item.getDonViTinh(), i, 2);
            tbl.setValueAt(item.getDonGia(), i, 3);
            tbl.setValueAt(item.getMaLoaiHang().getTenLoaiHang(), i, 4);
            i++;
        }
    }

    public void loadCombobox() {
        for (Danhmuchang item : model.danhmuchangs) {
            cbbLoaiHang.addItem(item.getTenLoaiHang());
        }
    }

    public void setJTable(Hanghoa item) {
        if (item == null) {
            JOptionPane.showMessageDialog(rootPane, "Không tìm thấy");
        } else {
            int i = 0;
            DefaultTableModel model = (DefaultTableModel) tbl.getModel();
            model.setRowCount(i + 1);
            tbl.setValueAt(item.getMaHang(), i, 0);
            tbl.setValueAt(item.getTenHang(), i, 1);
            tbl.setValueAt(item.getDonViTinh(), i, 2);
            tbl.setValueAt(item.getDonGia(), i, 3);
            tbl.setValueAt(item.getMaLoaiHang().getTenLoaiHang(), i, 4);
        }
    }

